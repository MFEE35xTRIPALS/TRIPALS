import React, { useState } from "react";
import axios from "axios";
import { Link } from "react-router-dom";

const Login = () => {
  const [id, setId] = useState("");
  const [password, setpassword] = useState("");
  const [labelText, setLabelText] = useState("");

  const handleSubmit = async (event) => {
    event.preventDefault();

    try {
      const response = await axios.post(
        "http://localhost:8000/login2",
        {
          id: id,
          password: password,
        },
        {
          headers: {
            "Content-Type": "application/x-www-form-urlencoded",
          },
        }
      );

      console.log(response);
      console.log(id, password);
      const { status } = response.data;
      if (status === "notEnter") {
        setLabelText("尚未輸入");
      } else if (status === "success") {
        setLabelText("登入成功");
        localStorage.setItem('user', JSON.stringify(response.data.currentuser))
        // localStorage.removeItem('user');
        // setTimeout(() => {
        //   console.log('Timeout triggered');
        // }, 5 * 2 * 1000);
        if (response.data.currentuser[0].permission === 1) {
          window.location = "/client";
        } else if (response.data.currentuser[0].permission === 0) {
          window.location = "/admin";
        }
      } else if (status === "notExist") {
        setLabelText("尚未註冊");
      } else if (status === "fail") {
        setLabelText("密碼錯誤");
      }
    } catch (error) {
      if (axios.isAxiosError(error)) {
        // 處理 AxiosError
        console.log('Axios error:', error.message);
        console.log('HTTP status:', error.response);
      } else {
        // 處理其他錯誤
        console.log('其他錯誤:', error.message);
      }
    }
  };

  const google = () => {
    window.open("http://localhost:8000/auth/google", "_self");
  };
  const facebook = () => {
    window.open("http://localhost:8000/auth/facebook", "_self");
  };

  return (
    <div className="page">
      <div id="LoginPage">
        <h1>Welcome back to Tripals</h1>
        <input type="button" value="透過Google登入" onClick={google} />
        <input type="button" value="透過Facebook登入" onClick={facebook} />

        <div id="hrOr">
          <hr />
          <p>OR</p>
          <hr />
        </div>
        <form id="Loginform" onSubmit={handleSubmit}>
          <label htmlFor="id">信箱</label>
          <input
            name="id"
            type="text"
            value={id}
            onChange={(event) => setId(event.target.value)}
          />
          <label htmlFor="password">密碼</label>
          <input
            name="password"
            type="text"
            value={password}
            onChange={(event) => setpassword(event.target.value)}
          />
          <label id="checkLabel" htmlFor="">
            {labelText}
          </label>
          <input type="submit" value="登入" />
          <p>沒有Tripals帳號?</p>
          <Link to="/register">Sign up</Link>
        </form>
      </div>
    </div>
  );
};

export default Login;
